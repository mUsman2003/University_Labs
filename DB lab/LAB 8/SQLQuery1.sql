SELECT DEPT.*, AVG1.AVERAGE FROM DEPT JOIN 
	(SELECT DEPTNO, AVG(SAL) AS AVERAGE FROM EMP GROUP BY DEPTNO) AS AVG1 ON DEPT.DEPTNO = AVG1.DEPTNO;