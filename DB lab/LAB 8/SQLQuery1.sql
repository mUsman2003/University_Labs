SELECT EMP.EMPNO AS EMP_NUMBER, EMP.ENAME AS FULL_NAME, EMP.SAL AS Salary, DEPT.DEPTNO AS DEPT_ID,DEPT.LOC AS Location
	FROM DEPT JOIN EMP
	ON DEPT.DEPTNO = EMP.DEPTNO

--Q2--
SELECT COUNT(EMPNO) AS NO_OF_EMP,DEPT.LOC AS LOCATION FROM EMP JOIN DEPT
	ON DEPT.DEPTNO=EMP.DEPTNO
	GROUP BY LOC

--Q3--
SELECT DEPT.* FROM DEPT INNER JOIN EMP
	ON DEPT.DEPTNO=EMP.DEPTNO
	WHERE EMP.SAL=(SELECT MAX(SAL) FROM EMP)

