using namespace std;
//------------------------------------------------------Q1------------------------------------------------------//
template<typename T>
T* RemoveDups(T* t,int size)
{
	int temp = 0;
	for (int n = 0; n < size; n++)
	{
		for (int m = n+1; m < size; m++)
		{
			if (t[n] == t[m])
			{
				continue;
			}
			else
			{
				t[temp] = t[m];
				temp++;
			}
		}
	}
	for (int n = 0; n < size; n++)
		cout << t[n] << " ";
	return t;
}
